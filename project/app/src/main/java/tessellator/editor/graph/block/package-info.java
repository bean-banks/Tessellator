package tessellator.editor.graph.block;

/**
 * The 'block' package groups together all the classes at the block level of the
 * editor in one location. The classes categorise blocks into different types
 * such as squares and triangles exist in the 'category' sub package. The classes
 * which handle events that a triggered within blocks exist in the 'eventhandling'
 * sub package. The classes that create and spawn block of specified categories
 * are located in the 'factory' sub package. The classes responible for creating
 * the visual components of blocks are found in the 'theme' sub package. The remaining
 * classes such as the Block and Connector classes represent the core semantic parts
 * of a block.
 */